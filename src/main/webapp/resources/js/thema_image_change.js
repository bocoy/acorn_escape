//테마 설명 나타나도록 하는 script
$(document).ready(function(){
	$("#image1").show();
	$("#note1").hide();
	$("#image1").mouseover(function(){
		$("#image1").hide();
		$("#note1").show();
		$("#note1").mouseout(function(){
			$("#note1").hide();
			$("#image1").show();
		});
	});
	//div 사이즈 맞추기
	var imageWidth=$("#image1").width();
	var imageHeight=$("#image1").height();
	$("#note1").width(imageWidth);
	$("#note1").height(imageHeight);
});
